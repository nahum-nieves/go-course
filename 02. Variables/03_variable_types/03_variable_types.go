package main

import "fmt"

func main() {
	// Integer types
	name := "Alice"
	age := 30
	height := 1.75
	var isMarried bool = false
	fmt.Printf("Type of name is %T\n", name)
	fmt.Printf("Type of age is %T\n", age)
	fmt.Printf("Type of height is %T\n", height)
	fmt.Printf("Type of isMarried is %T\n", isMarried)
}